    public void plenarTaula(Sucursal s){
        ArrayList<Cliente> clientes = s.getClientes();
        Vector info;
        for(Cliente cl : clientes){
            info =  new Vector();
            info.add(cl.getDni());
            info.add(cl.getNom());
            info.add(cl.getAp());
            info.add(cl.getDir());
            info.add(cl.getTlf());
            info.add(cl.getNumC());
            modelTaula.addRow(info);
            jTable1.setModel(modelTaula);
        }
    }
