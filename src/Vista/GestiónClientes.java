    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Borrar cliente
        String pin;
        boolean b = false;
        int row = this.jTable1.getSelectedRow();
        if(row > -1){
            String dni = this.jTable1.getValueAt(row, 0).toString();
            Cliente c = (Cliente) this.s.buscarCliente(dni);
            if(!this.jPasswordField1.getText().equals("")){
                pin = this.jPasswordField1.getText();
            if(c.compPin(Integer.parseInt(pin))){
                try{
                        Controlador.GestionBanco.borrarCliente(this.s, c);
                        b = true;
                } catch (TieneCuentas ex) {
                        JOptionPane.showMessageDialog(this, ex);
                    }
                try {
                    if(b){
                        this.s.borrarCliente(c);
                        actualitzar();
                    }
                    } catch (NoExisteCliente ex) {
                        JOptionPane.showMessageDialog(this, ex);
                    }
            }else{
                this.jPasswordField1.setText("");
                JOptionPane.showMessageDialog(this, "Pin incorrecto");
            }
            }else{
                JOptionPane.showMessageDialog(this, "Introduce el pin de "+c.getNom());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Debes seleccionar un cliente");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

