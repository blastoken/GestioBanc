    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Eliminar Cuenta
        CuentaCorriente cc;
        CuentaVivienda cv;
        FondoInversion fi;
        int row = this.jTable1.getSelectedRow();
        if(row != -1){
        String tipo = this.jTable1.getValueAt(row, 1).toString(),numC = this.jTable1.getValueAt(row, 0).toString();
        if(tipo.equals("Cuenta Corriente")){
            cc = (CuentaCorriente) this.c.buscarCuenta(Integer.parseInt(numC));
            try{
                this.c.borrarCuenta(cc);
                Controlador.GestionBanco.borrarCuenta(this.s, this.c, Integer.parseInt(numC));
                actualitzar();
            }catch(NoExisteCuenta ex) {
                JOptionPane.showMessageDialog(this,ex);
            }
        }else if(tipo.equals("Cuenta Vivienda")){
            cv = (CuentaVivienda) this.c.buscarCuenta(Integer.parseInt(numC));
            try{
                this.c.borrarCuenta(cv);
                Controlador.GestionBanco.borrarCuenta(this.s, this.c, Integer.parseInt(numC));
                actualitzar();
            }catch(NoExisteCuenta ex) {
                JOptionPane.showMessageDialog(this,ex);
            }
        }else if(tipo.equals("Fondo Inversión")){
            fi = (FondoInversion) this.c.buscarCuenta(Integer.parseInt(numC));
            try{
                this.c.borrarCuenta(fi);
                Controlador.GestionBanco.borrarCuenta(this.s, this.c, Integer.parseInt(numC));
                actualitzar();
            }catch(NoExisteCuenta ex) {
                JOptionPane.showMessageDialog(this,ex);
            }
        }
        }else{
            JOptionPane.showMessageDialog(this,"Debes seleccionar una cuenta");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

