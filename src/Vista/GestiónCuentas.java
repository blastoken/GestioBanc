    public void plenarTaula(){
        Vector info;
        CuentaCorriente cc;
        CuentaVivienda cv;
        FondoInversion fi;
        for(int i = 0; i < this.c.getCuentas().size(); i++){
            info =  new Vector();
            if(this.c.getCuentas().get(i) instanceof CuentaCorriente){
                cc = (CuentaCorriente) this.c.getCuentas().get(i);
                info.add(cc.getNumCuenta());
                info.add("Cuenta Corriente");
                info.add(cc.getSaldo());
                info.add(cc.getPuntos());
                modelTaula.addRow(info);
            }else if(this.c.getCuentas().get(i) instanceof CuentaVivienda){
                cv = (CuentaVivienda) this.c.getCuentas().get(i);
                info.add(cv.getNumCuenta());
                info.add("Cuenta Vivienda");
                info.add(cv.getSaldo());
                info.add(cv.getPuntos());
                modelTaula.addRow(info);
            }else if(this.c.getCuentas().get(i) instanceof FondoInversion){
                fi =  (FondoInversion) this.c.getCuentas().get(i);
                info.add(fi.getNumCuenta());
                info.add("Fondo Inversión");
                info.add(fi.getSaldo());
                info.add(fi.getPuntos());
                modelTaula.addRow(info);
            }
            jTable1.setModel(modelTaula);
        }
    }
