    public String corregirSaldo(){
        String saldo = Double.toString(gc.getBanco().calcBolsa());
        boolean b = false;
        String decimals = "", entero = "";
        for(int i = 0; i < saldo.length(); i++){
            if(b){
                decimals += saldo.charAt(i);
            }else{
                entero += saldo.charAt(i);
            }
            if(saldo.charAt(i) == '.'){
                b = true;
            }
        }
        if(decimals.length() == 0){
            entero+="00";
        }else if(decimals.length() == 1){
            entero+=decimals.charAt(0)+"0";
        }else if(decimals.length() >= 2){
            entero+=decimals.charAt(0)+""+decimals.charAt(1);
        }
        return entero;
    }
    
    public void plenarTaula(Banco b){
        ArrayList<Sucursal> sucursales = b.getSucursales();
        Vector info;
        for(Sucursal suc : sucursales){
            info =  new Vector();
            info.add(suc.getCod());
            info.add(suc.getLoc());
            info.add(suc.getClientes().size());
            modelTaula.addRow(info);
            jTable1.setModel(modelTaula);
        }
    }
    
