    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Eliminar Sucursal
        int row = this.jTable1.getSelectedRow();
        if(row > -1){
        Sucursal s = this.gc.getBanco().buscarSucursal(1+row);
            try {
                this.gc.getBanco().borrarSucursal(s);
                this.gc.borrarSucursal(s);
                JPanel jp = new JPanel();
                modelTaula = new DefaultTableModel();
                modelTaula.addColumn("Nº Sucursal");
                modelTaula.addColumn("Localidad");
                modelTaula.addColumn("Nº Clientes");
                jTable1.setModel(modelTaula);
                plenarTaula(gc.getBanco());
            } catch (NoExisteSucursal ex) {
                JOptionPane.showMessageDialog(this, ex);
            } catch (TieneClientes ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Debes seleccionar una sucursal");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

